    public void DateHeure() {
    // create a new thread using the Thread class and override its run() method
    Thread tr = new Thread() {
        public void run() {
            // infinite loop to continuously update the date and time
            for (;;) {
                try {
                    // get the current date and time using the Calendar class
                    Calendar cal = new GregorianCalendar();
                    
                    int annee = cal.get(Calendar.YEAR);
                    int moi = cal.get(Calendar.MONTH) + 1; 
                    int jour = cal.get(Calendar.DAY_OF_MONTH);
                    int heure = cal.get(Calendar.HOUR_OF_DAY);
                    int minute = cal.get(Calendar.MINUTE);
                    int seconde = cal.get(Calendar.SECOND);
                    
                    // update the text displayed in the two text fields
                    txtdate.setText(annee + "/" + moi + "/" + jour);
                    txtheure.setText(heure + ":" + minute + ":" + seconde);
                    
                    // pause the thread execution for 1000 milliseconds (i.e., 1 second)
                    sleep(1000);
                } catch (Exception e) {
                    // print the stack trace if an exception occurs
                    e.printStackTrace();
                }
            }
        }
    };
    
    // start the thread
    tr.start();
}

    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
